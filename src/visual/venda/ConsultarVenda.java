    public void atualizarTabela(){
        Cliente cliente;
        cliente = (Cliente) cbCliente.getSelectedItem();
        System.out.println(cliente.getId());
        List<Venda> lista = new DAOVenda().consultaPorCliente(cliente.getId());
        DefaultTableModel model = (DefaultTableModel) tbVenda.getModel();
        model.setNumRows(0);
        for (Venda venda: lista){
            model.addRow(venda.getDadosModel());
        }
    }

