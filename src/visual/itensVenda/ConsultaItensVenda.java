    public void atualizarTabela(){
        Venda venda;
        venda = (Venda) cbVenda.getSelectedItem();
        List<ItensVenda> lista = new DAOItensVenda().consultaPorVenda(venda.getId());
        DefaultTableModel model = (DefaultTableModel) tbItemVenda.getModel();
        model.setNumRows(0);
        for(ItensVenda i : lista){
            model.addRow(i.getDadosModel());
        }
    }

