    private void botaoIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoIncluirActionPerformed
        ItensCompra itenscompra = new ItensCompra();
        boolean inserir = true;
        try{
            itenscompra.setQuantidade(Double.parseDouble(campoQuantidade.getText()));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro no sal√°rio!");
            campoQuantidade.requestFocus();
            inserir = false;
        }
        itenscompra.setProduto((Produto) comboProduto.getSelectedItem());
        
        if(inserir){
            listaItens.add(itenscompra);
        }
        cadCompra.valor += itenscompra.getProduto().getPreco() * itenscompra.getQuantidade();
        cadCompra.alteraValor();
        limpaTela();
    }//GEN-LAST:event_botaoIncluirActionPerformed

